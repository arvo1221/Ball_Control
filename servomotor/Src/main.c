void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
  if(htim == &htim9)
  {
    //HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_5);
    current[0] = 100*(rx_buffer[0]-48) + 10*(rx_buffer[1]-48) + (rx_buffer[2]-48);
    current[1] = 100*(rx_buffer[3]-48) + 10*(rx_buffer[4]-48) + (rx_buffer[5]-48);
    target[0] = 100*(rx_buffer[6]-48) + 10*(rx_buffer[7]-48) + (rx_buffer[8]-48);
    target[1] = 100*(rx_buffer[9]-48) + 10*(rx_buffer[10]-48) + (rx_buffer[11]-48);
    
    for(i = 0; i <2; i++) {
      error[i] = (double)target[i] - current[i];
      I_control[i] += (double)error[i] * dt;
      control[i] = (double)(Kp[i]*error[i] + I_control[i] + Kd[i]*(error[i] - error_pre[i])/dt)/62.749;
      error_pre[i] = error[i];
    }
    
    degree[1] = 23-control[1];
    degree[3] = 23+control[1];

    degree[0] = 23+control[0];
    degree[2] = 23-control[0];
    
    if(current[1]<5) {
      degree[0] = 23;
      degree[1] = 23;
      degree[2] = 23;
      degree[3] = 23;
      for(i = 0; i <2; i++) {
      I_control[i] = 0;
      control[i] = 0;
      error[i] = 0;
      }
    }
    
    
    Motor_CCR1 = SERVO1(degree[0]);
    Motor_CCR2 = SERVO2(degree[1]);
    Motor_CCR3 = SERVO3(degree[2]);
    Motor_CCR4 = SERVO4(degree[3]);
    
    TIM1->CCR1 = Motor_CCR1;
    TIM1->CCR2 = Motor_CCR2;
    TIM1->CCR3 = Motor_CCR3;
    TIM1->CCR4 = Motor_CCR4;
   
  }
}


