int SERVO1(double sv1) {
  return (int)round(0.000004*pow(sv1,4)-0.0003*pow(sv1,3)+0.0071*pow(sv1,2)+0.9508*sv1+117.03);
}
int SERVO2(double sv2) {
  return (int)round(-0.000005*pow(sv2,4)+0.0005*pow(sv2,3)-0.012*pow(sv2,2)-0.9505*sv2+121.02);
}
int SERVO3(double sv3) {
  return (int)round(0.000004*pow(sv3,4)-0.0003*pow(sv3,3)+0.0065*pow(sv3,2)+1.0307*sv3+121.95);
}
int SERVO4(double sv4) {
  return (int)round(-0.0000007*pow(sv4,4)+0.00006*pow(sv4,3)+0.0001*pow(sv4,2)-1.0869*sv4+112.82);
}

